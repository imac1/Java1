package Cap2_10Conversii;

public class StringToInteger {
    public static void main(String args[]) {

        //Declarare variabila de tip String
        String numar = "300";

        //Convertirea variabilei String in Int folosind metoda Integer.parseInt()
        int i = Integer.parseInt(numar);

        //Afisarea valorii i
        System.out.println(i);
    }
}
